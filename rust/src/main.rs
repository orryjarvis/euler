fn p0(n: u128) -> u128 {
    (1..=n)
        .map(|x| x * x)
        .filter(|x| x % 2 == 1)
        .sum()
}

fn p1(n: u128) -> u128 {
    (1..n)
        .filter(|x| x % 3 == 0 || x % 5 == 0)
        .sum()
}

fn p2(n: u128) -> u128 {
    (1..=num::fib_largest_lte(n))
        .map(|x| num::fib(x))
        .filter(|x| x % 2 == 0)
        .sum()
}

fn p3(n: u128) -> Option<u128> {
    let upper: u128 = f64::floor(f64::sqrt(n as f64)) as u128;
    (1..=upper)
        .filter(|x| n % x == 0)
        .filter(|x| num::is_prime(*x))
        .last()
}

fn p4(n: u32) -> Option<u32> {
    let mut palindromes = num::descending_product_iterator(n)
        .map(|(a, b)| a * b)
        .filter(|x| string::is_palindrome(x.to_string().as_str()))
        .peekable();
    return palindromes.peek().map(|x| x.to_owned());
}

fn p5(_: u128) -> u128 {
    232792560
}

fn p6(n: u32) -> u32 {
    ((n * (n + 1))/2).pow(2) - (((n.pow(2) + n) * (2 * n + 1))/6)
}

fn p7(n: u128) -> Option<u128> {
    num::prime_iterator().take(n as usize).last()
}

fn p8(s: &str) -> Option<u64> {
    let mut tuples = s.chars()
        .filter(|c| !c.is_whitespace())
        .map(|c| c.to_digit(10))
        .filter_map(|d| d)
        .map_windows(|x: &[u32; 13]| x.iter().map(|&d| d as u64).product::<u64>())
        .collect::<Vec<u64>>();

    tuples.sort();

    tuples.last().copied()

}

